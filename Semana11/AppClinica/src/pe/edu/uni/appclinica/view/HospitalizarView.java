   private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
		try {
			// Datos del formulario
			IngresoModel model = new IngresoModel();
			model.setIdpaciente(pacienteModel.getIdpaciente());
			model.setFecha(txtFecha.getText());
			model.setGlosa(txtGlosa.getText());
			model.setHabitacion(Integer.parseInt(txtHabitacion.getText()));
			// Dato de la sesion
			PersonalModel personalModel;
			personalModel = (PersonalModel) Session.get("USUARIO");
			model.setIdpersonal(personalModel.getIdpersonal());
			// Proceso
			control.procRegistrarIngreso(model);
			// Limpiar controles
			txtIdPaciente.setText("");
			txtNombrePaciente.setText("");
			txtFecha.setText("");
			txtHabitacion.setText("");
			txtGlosa.setText("");
		} catch (Exception e) {
			JOptionPane.showMessageDialog(rootPane, e.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
		}
   }//GEN-LAST:event_btnAceptarActionPerformed

   private void btnBuscarPacienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarPacienteActionPerformed
		Session.put("PACIENTE", null);
		BuscarPacienteView view = new BuscarPacienteView(null, true);
		view.setVisible(true);
		if (Session.get("PACIENTE") == null) {
			return;
		}
		pacienteModel = (PacienteModel) Session.get("PACIENTE");
		txtIdPaciente.setText(pacienteModel.getIdpaciente() + "");
		txtNombrePaciente.setText(pacienteModel.getNombre() + ", " + pacienteModel.getApellido());
   }//GEN-LAST:event_btnBuscarPacienteActionPerformed


